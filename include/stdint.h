#ifndef H_STDINT
#define H_STDINT
typedef signed char int8_t; // int8_t	-	signed 8-bit integer
typedef unsigned char uint8_t;  // uint8_t	-	unsigned 8-bit integer
typedef signed short int16_t;   // int16_t	-	signed 16-bit integer
typedef unsigned short uint16_t;    // uint16_t -	unsigned 16-bit integer
typedef signed int int32_t; // int32_t	-	signed 32-bit integer
typedef unsigned int uint32_t;  // uint32_t -	unsigned 32-bit integer
#endif